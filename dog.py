class Dog():
    def __init__(self, name, age):
        self.name = name
        self.age = age
    
    def sit(self):
        print(f'{self.name} is sitting')

    def roll_over(self):
        print(f'{self.name} is rolling over')

my_dog = Dog('Sharik', 10)
my_dog.sit()


